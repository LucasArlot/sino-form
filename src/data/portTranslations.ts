// Helper function to get translated port name
export const getTranslatedPortName = (
  portCode: string, 
  userLang: 'en' | 'fr' | 'zh' | 'de' | 'es' | 'it' | 'nl' | 'ar' | 'pt' | 'tr' | 'ru',
  fallbackName?: string
): string => {
  const translations = PORT_TRANSLATIONS[portCode];
  if (translations && translations[userLang]) {
    return translations[userLang];
  }
  // Fallback to English if translation not found
  if (translations && translations.en) {
    return translations.en;
  }
  // Final fallback to provided name or port code
  return fallbackName || portCode;
}; 